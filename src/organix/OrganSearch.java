    public OrganSearch() {
        jdbc = new JDBC();
        initComponents();
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows if any

        try {
            this.jdbc.rs = this.jdbc.stmt.executeQuery("SELECT d.did, d.status, u.uid,o.oid,o.organ_name, u.email, u.name, u.blood_type, u.gender FROM donation d INNER JOIN user u on d.uid = u.uid INNER JOIN organ o on d.oid = o.oid ORDER BY organ_name;");

            while (this.jdbc.rs.next()) {
                Object[] row = new Object[9];
                row[0] = this.jdbc.rs.getInt("did");
                row[1] = this.jdbc.rs.getString("status");
                row[2] = this.jdbc.rs.getInt("uid");
                row[3] = this.jdbc.rs.getInt("oid");
                row[4] = this.jdbc.rs.getString("organ_name");
                row[5] = this.jdbc.rs.getString("email");
                row[6] = this.jdbc.rs.getString("name");
                row[7] = this.jdbc.rs.getString("blood_type");
                row[8] = this.jdbc.rs.getString("gender");
                model.addRow(row);
            }
        } catch (SQLException e) {
            System.out.println("Error fetching data: " + e.getMessage());
}

    }

